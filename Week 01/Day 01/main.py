# Day 1 practice: values, variables, types, printing, and comments.

# Starting score example: we change it later to prove reassignment works.
integer_var = 6
integer_var = 69  # score updated after a bonus

float_var = 3.14  # using a non-integer to confirm float behavior
string_var = "This is a string"
bool_var = True  # flip to False to see boolean output

# Print values
print(integer_var)
print(float_var)
print(string_var)
print(bool_var)

# Print types (microscope)
print(type(integer_var))
print(type(float_var))
print(type(string_var))
print(type(bool_var))

# String vs number: same-looking value, different type
var_10_str = "10"
var_10_int = 10

print(var_10_str)
print(var_10_int)
